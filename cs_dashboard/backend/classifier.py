def classify(memo: str) -> tuple:
    """call_memo → (new_category_main, new_category_sub). 미분류는 (None, None)."""
    if not memo:
        return None, None

    matched_subs = []
    for sub, keywords in RULES:
        for kw in keywords:
            if kw in memo:
                matched_subs.append(sub)
                break

    if not matched_subs:
        return None, None

    non_fallback = [s for s in matched_subs if s not in FALLBACK_SUBS]
    fallback_only = len(non_fallback) == 0
    candidates = non_fallback if non_fallback else matched_subs

    main_groups: dict = {}
    for sub in candidates:
        main = SUB_TO_MAIN[sub]
        if main not in main_groups:
            main_groups[main] = sub  # rules 앞쪽 우선

    if len(main_groups) > 1:
        if fallback_only:
            first_main = next(iter(main_groups))
            return first_main, main_groups[first_main]
        for priority_main in ("기타", "해지·유지 상담", "네트워크·앱 오류", "기기·하드웨어 오류", "미납·결제", "체험 관련", "교재·물류·배송", "계정·서비스", "윙크북스"):
            if priority_main in main_groups:
                return priority_main, main_groups[priority_main]
        return "기타", "기타"

    main = next(iter(main_groups))
    return main, main_groups[main]
